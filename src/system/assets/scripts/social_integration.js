$(document).ready(function() {
    // Facebook Integration.
    
    $("#facebook-like-button a").remove();
    
    $("#facebook-share-button a").remove();
    
    $('head').prepend('<script>(function(d, s, id) { var js, fjs = d.getElementsByTagName(s)[0]; if (d.getElementById(id)) return; js = d.createElement(s); js.id = id; js.src = "http://connect.facebook.net/en_US/sdk.js#xfbml=1&version=v2.0"; fjs.parentNode.insertBefore(js, fjs); }(document, \'script\', \'facebook-jssdk\'));</script>');
    
    // Twitter Integration.
    
    $('head').prepend('<script>!function(d,s,id){var js,fjs=d.getElementsByTagName(s)[0];if(!d.getElementById(id)){js=d.createElement(s);js.id=id;js.src="https://platform.twitter.com/widgets.js";fjs.parentNode.insertBefore(js,fjs);}}(document,"script","twitter-wjs");</script>');
    
    // Google Integration.
    
    $('head').prepend('<script type="text/javascript" src="https://apis.google.com/js/plusone.js"></script>');
});